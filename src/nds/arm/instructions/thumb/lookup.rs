#[cfg(not(feature = "epic"))]
pub fn lookup_instruction<const ARM_BOOL: bool>(
    ctx: &mut Context<Instruction, impl ContextTrait>,
) -> u32 {
    let inst_set = ctx.inst.get_halfword(6, 15);
    run_instruction::<ARM_BOOL>(inst_set, ctx)
}

